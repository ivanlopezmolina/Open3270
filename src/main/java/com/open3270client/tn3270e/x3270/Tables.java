    /** Converts a single EBCDIC byte to ASCII using the {@link #EBC_2_ASCII} table. */
    public static char ebcdicToAscii(byte ebcdicByte) {
        return (char) (EBC_2_ASCII[ebcdicByte & 0xff] & 0xff);
    }

    /** Converts a single ASCII byte to EBCDIC using the {@link #A2E} table. */
    public static byte asciiToEbcdic(byte asciiByte) {
        return A2E[asciiByte & 0xff];
    }

